const sleep = ms => new Promise(r => setTimeout(r, ms));

async function run() {
  console.log(`Emailing ${users.length} students...`);
  let sent = 0, failed = 0;

  for (const [userId, email] of users) {
    try {
      const r = await fetch(`${API_URL}/api/admin/games/${GAME_ID}/email-one`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json', 'Cookie': `token=${ADMIN_TOKEN}` },
        body: JSON.stringify({ userId }),
      });
      const text = await r.text();
      let data;
      try { data = JSON.parse(text); } catch(e) { console.log(`\nBad JSON for ${email}:`, text.slice(0,100)); failed++; continue; }
      if (data.success) {
        sent++;
        process.stdout.write('.');
      } else {
        failed++;
        console.log(`\nFailed: ${email} - ${data.error}`);
      }
    } catch (err) {
      failed++;
      console.log(`\nError: ${email}: ${err.message}`);
    }
    await sleep(2000);
  }
  console.log(`\n✅ Sent: ${sent}, Failed: ${failed}`);
  console.log('Done!');
}

run().catch(console.error);
